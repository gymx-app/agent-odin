import type { Exercise } from '../../src/domain/exercise/exercise.types.js';
import {
  MOVEMENT_DEMAND_TAGS,
  type MovementDemandTag,
} from '../../src/domain/exercise/exercise-taxonomy.js';

type ExerciseInput = Omit<
  Exercise,
  | 'status'
  | 'fatigue_cost'
  | 'movement_demands'
  | 'default_rest_seconds'
  | 'contraindication_tags'
  | 'coaching_notes'
  | 'aliases'
> & {
  fatigue_cost?: Exercise['fatigue_cost'];
  movement_demands?: Partial<Record<MovementDemandTag, number>>;
  default_rest_seconds?: Exercise['default_rest_seconds'];
  contraindication_tags?: MovementDemandTag[];
  coaching_notes?: string[];
  aliases?: string[];
};

const zeroMovementDemands = (): Exercise['movement_demands'] =>
  Object.fromEntries(
    MOVEMENT_DEMAND_TAGS.map((tag) => [tag, 0]),
  ) as Exercise['movement_demands'];

const createExercise = ({
  fatigue_cost,
  movement_demands,
  default_rest_seconds,
  contraindication_tags,
  coaching_notes,
  aliases,
  ...input
}: ExerciseInput): Exercise => ({
  ...input,
  status: 'active',
  fatigue_cost: {
    systemic: 2,
    local: 2,
    axial: 1,
    grip: 1,
    ...fatigue_cost,
  },
  movement_demands: {
    ...zeroMovementDemands(),
    ...movement_demands,
  },
  default_rest_seconds: default_rest_seconds ?? {
    min: 60,
    max: 120,
  },
  contraindication_tags: contraindication_tags ?? [],
  coaching_notes: coaching_notes ?? [],
  aliases: aliases ?? [],
});

